package SnakeAndLadderGame.Board;

public class Box {
    private int id;
    private int inc;
    private int dec;

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public int getInc() {
        return inc;
    }
    public void setInc(int inc) {
        this.inc = inc;
    }
    public int getDec() {
        return dec;
    }
    public void setDec(int dec) {
        this.dec = dec;
    }    
}
